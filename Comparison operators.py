#Comparison/Relational operators

#Equal to ( == )
print(7==7) #True
print(7==6) # False
print("-------------------------------")

#Not Equal to ( != )
print(7!=7) #False
print(7!=6) #True
print("-------------------------------")

#Less Than Equal to ( <= )
print(7<=7) #True
print(7<=6) #False
print(10<=12) #True
print("------------------------------")

#Greather Than Equal to ( >= )
print(7>=7) #True
print(7>=6) #True
print(10>=12) #False
print("------------------------------")

#Less Than ( < )
print(7<7) #False
print(7<6) #False
print(10<12) #True
print("------------------------------")

#Greather Than ( > )
print(7>7) #False
print(7>6) #True
print(10>12) #False
print("------------------------------")


