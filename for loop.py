#For Loop

#print 1 to 10 usinng for loop
for i in range(1,11):
    print(i)
print()

#print 10 to 1 reverse using for loop
for i in range(10,0,-1):
    print(i)
print()

#print 2's table using for loop
for i in range(2,21,2):
    print(i)


#for loop with else
for i in range(1,11):
    print(i)
else:
    print("Else block")